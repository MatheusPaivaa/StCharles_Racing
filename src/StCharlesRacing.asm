startGame:
  call ApagaTela ; Apaga a tela principal

  loadn R0, #mapa_principal ; Carrega o mapa principal
  call prinTela ; Imprime o mapa principal

  ; Definir posio inicial do personagem principal
  loadn R0, #1019

  store posCarro, R0 ; Posio inicial do personagem principal
  store posAntCarro, R0 ; Posio inicial do personagem principal (anterior)

  loadn R0, #0			; Contador para os Mods	= 0
	loadn R2, #0			; Para verificar se (mod(c/10)==0
  loadn R3, #0			; Contador de Pontos
  load R4, dif			; Dificuldade

  cmp R4, R0
  ceq ajusteDif ; Se a dificuldade for 0, ajusta para 1

  call desenhaCarro ; Desenha o carro na posio inicial

  loopPrincipal:

      loadn R1, #20 ; Podemos ajustar a velocidade do carro aqui
      mod R1, R0, R1
      cmp R1, R2		; if (mod(c/10)==0
      ceq MoveCarro	; Chama Rotina de movimentacao da Nave

      loadn R1, #150
      mul R1, R1, R4
      mod R1, R0, R1
      cmp R1, R2		; if (mod(c/150 * R4)==0
      ceq Pontua    ; Chama Rotina de Pontuao

      call Delay          ; Adiciona um atraso para suavizar a movimentao
      inc R0 ; Incrementa o contador de Mods
      jmp loopPrincipal ; Loop

  rts

